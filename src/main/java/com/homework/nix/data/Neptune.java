package com.homework.nix.data;

public class Neptune {

    String neptuneName = "Neptune";
    static double weight = 1.02 * Math.pow(10, 26);
    static double radius = 2.45 * Math.pow(10, 7);

    public String getNeptuneName() {
        return neptuneName;
    }

    public double getWeight() {
        return weight;
    }

    public double getRadius() {
        return radius;
    }
}
